// Day_02: Operators

/* ----- Arithmetic Operations ----- */

const num1 = 7;
const num2 = 4;

// Task-1: add two numbers.
const add = num1 + num2;
console.log(add); // output:- 11

// Task-2: subtract two numbers.
const sub = num1 - num2;
console.log(sub); // output:- 3

// Task-3: multiply two numbers.
const mul = num1 * num2;
console.log(mul); // output:- 28

// Task-4: divide two numbers.
const div = num1 / num2;
console.log(div); // output:- 1.75

// Task-5: find the reminder when one-number is divided by another.
const mod = num1 % num2;
console.log(mod); // output:- 3
