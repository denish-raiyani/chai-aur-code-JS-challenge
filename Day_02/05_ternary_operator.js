/* ----- Conditional (ternary) Operators ----- */

// Task-14: use the ternary operator to check if a number is positive or negative.
const num = -5;
console.log(num > 0 ? "Number is Positive" : "Number is Negative"); // output:- Number is Negative

const age = 18;
console.log(age >= 18 ? "adult" : "minor"); // output:- adult
