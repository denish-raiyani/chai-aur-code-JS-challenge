// Day_02: Operators

/* ----- Assignment Operators ----- */

let num = 10;

// Task-6: Use "+=" operator to add a number to a variable.
num += 5;
console.log(num); // output:- 15

// Task-7: Use "-=" operator to subtract a number from a variable.
num -= 5;
console.log(num); // output:- 10

// NOTE: 'const' cannot be used with assignment operators. because the value of a constant can't be changed through reassignment using the Assignment Operator.
