/* ----- While Loop ----- */

// Task-3: calculate the sum of numbers from 1 to 10 using a while loop.
let sum = 0;
let i = 1;

while (i <= 10) {
  sum += i;
  i++;
}
console.log(`Sum of numbers from 1 to 10 = ${sum}`);
// output:- Sum of numbers from 1 to 10 = 55

// Task-4: print numbers from 10 to 1 using a while loop.
let j = 10;

while (j >= 1) {
  console.log(j);
  j--;
}
