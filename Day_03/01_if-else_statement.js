/* ----- If-Else Statements ----- */

// Task-1: to check if a number is positive, negative, or zero.
const num = 5;

if (num > 0) {
  console.log(`Number is Positive`);
} else if (num < 0) {
  console.log(`Number is Negative`);
} else {
  console.log(`Number is Zero`);
}
// output:- Number is Positive

