/* ----- Conditional (ternary) Operators ----- */

// Task-4: use the ternary operator to check if number is even or odd.
const num1 = 89;
console.log(num1 % 2 === 0 ? `Number ${num1} is even` : `Number ${num1} is odd`);
// output:- Number 89 is odd

const num2 = 48;
console.log(num2 % 2 === 0 ? `Number ${num2} is even` : `Number ${num2} is odd`);
// output:- Number 48 is even
